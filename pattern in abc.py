#Pattern
#IN ALL CAPS
n=int(input("enter the number of rows"))
print('in all caps')

#increasing half triangle
print('increasing half triangle')
for i in range(n+1):
    for j in range(i+1):
        print(chr(65+j),end="")
    print()
#decreasing half triangle
print('decreasing half triangle')
for i in range(n):
    for j in range(n-i):
        print(chr(65+j),end='')
    print()

#right sided triangle increaing
print('right sided triangle increaing')
for i in range(n):
    for j in range(n-i):
        print(' ',end=' ')
    for k in range(i):
        print(chr(65+k),end=' ')
    print(' ')
print()

#right sided triangle decreasing
print('right sided triangle decreasing')
for i in range(n):
    for j in range(i):
        print(' ',end=' ')
    for k in range(n-i):
        print(chr(65+k),end=' ')
    print(' ')
print()

#Half diamond 
print('half diamond')
for i in range(n):
    for j in range(n-i):
        print(' ',end=' ')
    for k in range(2*i-1):
        print(chr(65+k),end=' ')
    print()
print()

#half triangle reverse
print('half triangle reverse')
for i in range(n):
    for j in range(i):
        print(' ',end=' ')
    for k in range(2*(n-i)-1):
        print(chr(65+k),end=' ')
    print()
print()

